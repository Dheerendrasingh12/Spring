package com.dheerendra.bo;

public class CustomerBo {

	private int cno;
	private String cname;
	private float cprin;
	private float crate;
	private float ctime;
	private float csi;
	public int getCno() {
		return cno;
	}
	public void setCno(int cno) {
		this.cno = cno;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public float getCprin() {
		return cprin;
	}
	public void setCprin(float cprin) {
		this.cprin = cprin;
	}
	public float getCrate() {
		return crate;
	}
	public void setCrate(float crate) {
		this.crate = crate;
	}
	public float getCtime() {
		return ctime;
	}
	public void setCtime(float ctime) {
		this.ctime = ctime;
	}
	public float getCsi() {
		return csi;
	}
	public void setCsi(float csi) {
		this.csi = csi;
	}
	
}
